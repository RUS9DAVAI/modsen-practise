function sumOfSquares(arr) {
    // Инициализируем переменную для хранения суммы квадратов
    let sum = 0;

    // Проходимся по каждому элементу массива
    for (let i = 0; i < arr.length; i++) {
        // Добавляем квадрат текущего элемента к сумме
        sum += arr[i] * arr[i];
    }

    // Возвращаем сумму квадратов
    return sum;
}

// Пример использования функции
const numbers = [1, 2, 3, 4, 5];
console.log(sumOfSquares(numbers)); // Выводит 55, так как 1^2 + 2^2 + 3^2 + 4^2 + 5^2 = 55
